// = simbolo de atribuição atribui o valor presente a sua esquerda a variavel a direita 
const a = 7
let b = 3

// operação aritimética do valor a esquerda com o valor já presente na variavel 

b += a // b = b+a
console.log(b)

b -= 4 // b = b-4
console.log(b)

b *= 2 // b = b*2
console.log(b)

b /= 2 // b = b/2
console.log(b)

b %= 2 // b = b%2
console.log(b)
